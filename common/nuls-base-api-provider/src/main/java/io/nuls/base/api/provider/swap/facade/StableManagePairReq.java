package io.nuls.base.api.provider.swap.facade;

import io.nuls.base.api.provider.BaseReq;

/**
 * @author: PierreLuo
 * @date: 2021/7/1
 */
public class StableManagePairReq extends BaseReq {

    /**
     * 提案类型
     */
    private int type;
    /**
     * 稳定币交易对地址
     */
    private String businessAddress;
    private String content;
    private String address;
    private String password;

    public StableManagePairReq() {
    }

    public StableManagePairReq(int type, String businessAddress, String content, String address) {
        this.type = type;
        this.businessAddress = businessAddress;
        this.content = content;
        this.address = address;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getBusinessAddress() {
        return businessAddress;
    }

    public void setBusinessAddress(String businessAddress) {
        this.businessAddress = businessAddress;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
